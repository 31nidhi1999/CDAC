package in.cdac.p1;

public class classA {
	private int num1 = 10;
	int num2 = 20;
	protected int num3 = 30;
	public int num4=40;
	
	public void fun() {
		System.out.println("classA Num1 :"+this.num1);
		System.out.println("classA Num2 :"+this.num2);
		System.out.println("classA Num3 :"+this.num3);
		System.out.println("classA Num4 :"+this.num4);
	}
}
